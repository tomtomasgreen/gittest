	public function add($data) {
		$createtime = time();
		$createtimes = date('Y-m-d H:i:s', $createtime);
		$data['bindtime'] = $createtime;
		$data['bindtimes'] = $createtimes;
		$data['bindip'] = nn_get_ip();
